#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *sr;

	sr = s1;
	while (*sr)
		sr++;
	while (*s2)
		*sr++ = *s2++;
	*sr = '\0';
	return (s1);
}
